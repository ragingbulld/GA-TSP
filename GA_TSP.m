ObjV = PathLength(D, Chrom); % 计算初始路径长度
preObjV = min(ObjV);

while gen < MAXGEN
    % A. 计算适应度
    FitnV = Fitness(ObjV);
    
    % B. 选择
    SelCh = Select(Chrom, FitnV, GGAP);
    
    % C. 交叉
    SelCh = Recombin(SelCh, Pc);
    
    % D. 变异
    SelCh = Mutate(SelCh, Pm);
    
    % E. 进化逆转 (针对 TSP 的局部搜索增强)
    SelCh = Reverse(SelCh, D);
    
    % F. 重插入
    ObjVSel = PathLength(D, SelCh);
    Chrom = Reins(Chrom, SelCh, ObjV);
    
    % G. 更新代数并记录最优解
    gen = gen + 1;
    ObjV = PathLength(D, Chrom);
    [minObjV, minInd] = min(ObjV);
    trace(gen) = minObjV; % 存入追踪数组

    if gen > 1 && trace(gen) < trace(gen-1)
        conv_gen = gen; % 更新最后一次变优的代数
        conv_time = toc(total_tic);
    end
    
    % H. 动态更新进化曲线 [关键修改]
    % 这种方式即便你在运行中点击窗口，曲线也不会断开
    set(hPlot, 'XData', 1:gen, 'YData', trace(1:gen));
    
    % 动态调整坐标轴范围，方便观察
    if gen > 1
        % Y轴范围留出 10% 的余量
        ylim([minObjV * 0.95, max(trace(1:gen)) * 1.05]);
        xlim([1, MAXGEN]);
    end
    
    drawnow; % 强制刷新图形窗口
end

