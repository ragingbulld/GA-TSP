trace = zeros(1, MAXGEN); % 用于存储每一代的最优解，确保曲线连续 [关键修改]
conv_gen = 0;
conv_time = 0;                 % 记录收敛时花费的时间

% 预先配置优化过程图
figure('Name', '遗传算法优化过程', 'NumberTitle', 'off');
