NIND = 100;       % 种群大小
MAXGEN = 500;     % 最大迭代次数
Pc = 0.9;         % 交叉概率
Pm = 0.1;        % 变异概率
GGAP = 0.9;       % 代沟 (generation gap)

D = Distance(X);  % 生成距离矩阵
N = size(D, 1);   % 城市规模

%% 2. 初始化种群
